#include "libftprintf.h"

void	ft_putnbr(int n)
{
	unsigned int i;

	if (n < 0)
	{
		i = -n;
		ft_putchar('-');
	}
	else
		i = n;
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		ft_putchar(i + '0');
}
